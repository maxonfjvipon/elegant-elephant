namespace ElegantBro\Stringify;

use ElegantBro\Interfaces\Stringify;

final class Just implements Stringify
{
    /**
     * @var string
     */
    private $value;

    public function __construct(string $value)
    {
        $this->value = $value;
    }

    /**
     * @return string
     */
    public function asString(): string
    {
        return $this->value;
    }
}
